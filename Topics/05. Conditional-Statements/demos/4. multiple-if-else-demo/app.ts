let ch: string = "X";

if (ch === "A" || ch === "a") {
    console.log("Vowel [ei]");
}
else if (ch === "E" || ch === "e") {
    console.log("Vowel [i:]");
}
else if (ch === "I" || ch === "i") {
    console.log("Vowel [ai]");
}
else if (ch === "O" || ch === "o") {
    console.log("Vowel [ou]");
}
else if (ch === "U" || ch === "u") {
    console.log("Vowel [ju:]");
}
else {
    console.log("Consonant");
}